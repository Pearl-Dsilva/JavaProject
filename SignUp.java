    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         String username = jTextField1.getText();
        String password = jTextField3.getText();
        String confirmPassword = jTextField2.getText();

        if (password.equals(confirmPassword)) {
            if (createUser(username, password)) {
                // Signup successful, display a success message or perform other actions
                JOptionPane.showMessageDialog(this, "Account created successfully", "Signup Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // Signup failed, display an error message
                JOptionPane.showMessageDialog(this, "Failed to create account", "Signup Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Passwords do not match, display an error message
            JOptionPane.showMessageDialog(this, "Passwords do not match", "Signup Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Method to create a new user
    private boolean createUser(String username, String password) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/User", "root", "64152737");
            String query = "INSERT INTO users_details (username, password) VALUES (?, ?)";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);
            int result = pst.executeUpdate();

            return result > 0; // Returns true if a new user is successfully created
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    
    }//GEN-LAST:event_jButton2ActionPerformed

